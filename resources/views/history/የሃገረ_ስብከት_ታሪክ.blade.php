            ብፁዕ አቡነ ቀሌምንጦስ የሰሜን ሸዋ ሀገረ ስብከት ሊቀ ጳጳስ ሆነው ከተመደቡ ወዲህ የሚሰጡትን አባታዊ
የሥራ መመሪያ ያሳልጡ የነበሩ ሥራ አስኪያጆችን ስንመለከት 1ኛ መልአከ ሣህል አባ ዘውዱ በየነ 2ኛ
መምህር ሣህለ ማርያም ወዳጆ 2ኛ መምህር ኃይሉ ጸጋው የነበሩ ሲሆን በአሁኑ ሰዓትም መጋቤ ሐዲስ ነቅዐ
ጥበብ አባቡ የሀገረ ስብከቱ ሥራ አስኪያጅ ሆነው በከፍተኛ የሓላፊነት መንፈስና የሥራ ትጋት እያገለገሉ
ይገኛሉ የሰሜን ሸዋ ሀገረ ስብከት በብፁዕ አቡነ ቀሌምንጦስ አባታዊ መመሪያ ሰጭነት ለተከናወኑት
ውጤታማ ሥራዎች ሥራ አስኪያጆቹ በየዘመናቸው በአከናወኑት ተግባራት ከፍተኛ ድርሻ አላቸው ፡፡
        </p>
        <p class='p'>
            ሰሜን ሺዋ የኢትዮጵያ ሐዲስ ሐዋርያ የእነ አቡነ ተክለ ሃይማኖት ፣ ቅድስት ክርስቶስ ሠምራ ፣ አቡነ
ዜና ማርቆስ ፣ አቡነ ሕፃን ሞዐ እና የሌሎችም በርካታ መካነ ቅዱሳን እና መካነ ምሁራን መገኛነው ።
ታሪካዊው የሰሜን ሸዋ ሀገረ ስብከት በታሪክ የታወቁ በዓለም የተደነቁ እንደ ጻድቃኔ ደብረ ምጥማቅ ማርያም
፣ ሸንኮራ ደብረ መንክራት ቅዱስ ዮሐንስ ፣ ሚዳ አቡነ መልከ ጼዴቅ ፣ አርብኣ ሐራ መድኃኔ ዓለም ፣ ደብረ
ብሥራት አቡነ ዜና ማርቆስ ፣ ዘብር ደብረ ምህረት ቅዱስ ገብርኤልና ሌሎችም ወዘተ … ያሉ ፈዋሽና
ታላላቅ ገቢረ ተአምራት የሚሠሩ ዕንቁ ገዳማት መገኛ ሲሆን እምቅ የተፈጥሮና ሰው ሠራሽ ሀብት ባለቤትም
ነው የዐፄ ዘርዐ ያዕቆብ መናገሻ ፣ የእምዬ ምኒልክ የትውልድ ቦታ ፣ የመጀመሪያው ኢትዮጵያዊ ፓትርያርክ
ብፁዕ አቡነ ባስልዮስ ፣ የእነ መምህር አካለ ወልድ ፣ መምህር ኪዳነ ወልድ ፣ የኔታ ተክሌ ፣ ብላቴን ጌታ
ኅሩይ ፣ ደራሲ ከበደ ሚካኤል ፣ የፊደል ገበታ አባት የእነ ተስፋ ገብረ ሥላሴ ዘብሔረ ቡልጋ፣ እጅግ በጣም
የተከበሩ የዓለም ሎሬት ሜትር አርቲስት አፈወርቅ ተክሌ ፣ ጸሐፌ ትእዛዝ አክሊሉ ሀብተ ወልድ ፣ ደራሲ
ተክለ ጻዲቅ መኩሪያ እና የጀግኖቹ የእነ ራስ አበበ አረጋዊ መፍለቂያ ማኅፀነ ለምለም የታሪክ መዳረሻ
ሲሆን ዛሬም ሃይማኖታዊ ባሕሉን ማንነቱንና ክብሩን ጠብቆ ታሪክ መሥራቱን ቀጥሏል ፡፡
        </p>
        <p class='p'>የታሪኩ ዋና ዋና ምንጮች ፡- የቤተ ክርስቲያን ታሪክ (በሉሌ መልአኩ) የኢትዮጵያ ኦርቶዶክስ ተዋሕዶ
            ቤተክርስቲያን ታሪክ (ብፁዕ አቡነ ጎርጎርዮስ)ዜና ጳጳሳት ኢትዮጵያውያን (በዲ/ን መርሻ አለኸኝ)ዝክረ አበው
            የብፁዕ አቡነ ኤፍሬም ታሪክ (በወሰን ደበበ ማደፍሮ) መጽሐፈ ሰዋስው ወግስ ወመዝገበ ቃላት ሐዲስ (አለቃ
            ኪዳነ ወልድ ክፍሌ)
        </p>
        <p class='right'>ምስጋና ፡- ለሊቀ ትጉሃን ኃይለ ጊዮርጊስ ዳኘ የቀድሞው የሰ/ሺ/ሀ/ስ/ሥራ አስኪያጅ
            - ለመጋቤ አእላፍ ማሙዬ ሸዋፈራ የቀቀድሞው የሰ/ሺ/ሀ/ስ/ሥራ አስኪያጅ</p>
