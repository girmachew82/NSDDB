    <p class="text-center">ሰሜን ሸዋ /ሺዋ/ ሀገረ ስብከት ከትናንት እሰከ ዛሬ</p>
    <hr>
    <p class='p'>"ሺዋ" የሚለውን ስያሜ የተለያዩ የታሪክ ጻሓፍትና ተርጓሚዎች በጽሑፋቸው በልዩ ልዩ አገላለጥ
        ሲተርኩት በሀገራችን የማይናቅ ሚና ያላቸው የአፈ ታሪክ ቅብብሎሽ ሲኖራቸው በጽሑፍ የሰፈረውን
        ታላቁ ኢትዮጵያዊ የሥነ - ጽሑፍ ሊቅ አለቃ ኪዳነ ወልድ ክፍሌ ‹‹መጽሐፈ ሰዋሰው ወግስ ወመዝገበ
        ቃላት ሐዲስ›› በተሰኘው መጽሐፋቸው በገጽ 861 ላይ "ሺዋ" ስለሚለው ስያሜ የሚከተለውን ብለው
        እናገኛለን፡፡ ሺዋ ማለት ስመ ሀገረ ብእሲት ሺኋ ሽኺቱ ምሥጢሩ ብዛት ብዙነት ነውና ሺሖ አሸዋ
        ከሚለው ይስማማል "ታሪክ" አንድ ባላባት ሴት ልጁን ለግሼ ባላበት ለወንድ ልጁ በሕግ አጋብቷት ስትኖር
        ከብዙ ዘመን በኋላ ልጆቿንና የልጅ ልጆቿን ይዛ አባቷን ለማየት ወደ አባቷቤት ኼደች ፡፡</p>
        <p class='p'>
            አባቷም በልጆቿ ብዛት ተደንቆ እነዚህ ኹሉ ምኖችሽ ናቸው ቢላት ልጆቼ እኩሉም የልጅ ልጆቼ አለችው
ልጄ ሺዋ ልጄ ሺዋ ቀድሞ ስድርሽ ሺ ኹኚ ሺ ያድርግሽ ብዬ መርቄሽ ነበር አሁንም እንደምርቃቴ ሺ
ስለኾንሽ ከእንግዲህ ወዲህ ስምሽ ሺዋ ይኹን ብየሻለሁ አላት፤ ርሷም እንደ ሣራ በቡራኬ ላይ ቡራኬ
ጨምራ ሺዋ ተብላ ተመለሰች ልጆቿም ሺዎች የሺዋ ልጆች ተብለው በየሀገሩ ተበትነው መንዝን ተጉለትን
ይፋትን ቡልጋን ጠራን መርሐ ቤቴን ሌላውንም ኹሉ አቀኑ፡፡ ከግሼ ከዳየር እስከ ዝቋላና እስከ የረር
ምሥራቁና ምዕራቡም ሳይቀር በርሷ ስም ሺዋ ተብሏል በማለት ሰፊማብራሪያ ሠጥተዋል፡፡ በዚህ ቋሚና
ሕያው ምስክር መሠረት ሺዋ የሚለው የጽሑፍ ይዘት በልማድ እንደምንናገረውና እንደምንጽፈው
በሸየመጀመሪያ ፊደል ሸዋ ሳይኾን ቁጥርን ብዛትን በሚያመለክተው በሣልሱ ፊደል ሺዋ መሆኑን
ስለሚያስረዳን ሸዋ ብለን የምንናገረውና የምንጽፈው ፍጹም ስሕተት በመሆኑ ለወደፊት ልናርም ይገባል፡፡
        </p>
        <p  class='p'>
            በተጨማሪም አሸዋ ተብሎ የተጠቀሰው ቃልም እንደ መጽሐፍ ቅዱስ እንደ ምድር አሸዋ እንደ ሰማይ
ከዋክብት ይብዙ ከሚለው የአበው በረከት እንጂ በቁሙ አሸዋ ላለማለት መሆኑን መገንዘብ ያስፈልጋል፡፡
ከዚህ አኳያ ሺዋ የተባለው ሀገር በየክፍለ ዘመኑ የነበሩ ፖለቲከኞች የተሸነሸነ ካርታ ሳይሰጡን ሺዋ
በቅርቡ እንኳን በግሼ ራቤል ከወሎ ዋየትና ከሚሴ ፣ በአንኮበር ከአፋር ፣ በየረርና ከርዩ ከሐረርጌ ፣
አዋሽና ባሌ ፣ በደቡብ ሻሸመኔ ጥቁር ውኃ ፣ በምዕራብ ግቤ ወንዝ በሰላሌ አባይ ድልድይ በመርሐቤቴ
ከጐጃም ወሎ የሚዋሰን የኢትዮጵያ ማዕከለ ምድር ነበር፡፡ መልክአ ምድሩ ለጦርነት ለወራሪ የማይመች
አባጣ ጐባጣ ተራራ ሸለቆና ሸንተረር ያለበት ሕዝቡም ተመሳሳይ የሆነ የጀግንነት ሥነ ልቦና እና ፈሪሀ
እግዚአብሔር ያለው አኗኗር ፣ አመጋገብ ፣ አለባበስ ፣ አስተሳሰብ ያለው ስልጡን ዐዋቂ ሥራ ወዳድ
ታታሪ ከሌላው ጋር አብሮ የመኖር ችሎታ ያለው ብልህ ሕዝብ ነው፡፡
        </p>
        <p  class='p'>
            ስለ ሺዋ ስያሜ ታሪካዊ አመጣጥና ስለ ሕዝቡ ምንነት ከብዙ በጥቂቱ ከጠቀስን ሰሜን ሺዋን ስለ አሰገኘው
ሺዋ ጥቂት ገለጣ ማድረግ አስፈላጊና ተገቢ ነው፡፡ በዚህም የሺዋ መናገሻ ከተማ አዲስ አበባ ፣ ተጉለትና
ቡልጋ ደብረ ብርሃን ፣ ይፋትና ጥሙጋ ኤፌሶን (አጣዬ) ፣ መንዝና ግሼ መሐል ሜዳ፣ መርሐቤቴ ዓለም
ከተማ፣ ሰላሌ ፍቼ፣ጅባትና ሜጫ አምቦ፣ ጨቦና ጉራጌ ወሊሶ፣ ከምባታና ሐዲያ ሆሣዕና፣ ሐይቆችና ቡታጅ
ራዝዋይ፣ የረርና ከረዩ ናዝሬት (አዳማ) ነበሩ፡፡
        </p>
        <p class='p'>
            ይኸ አወቃቀር ከንጉሠ ነገሥቱ ሥርዓት ወዲህ በደርግ እስከ 1981 ዓ.ም የነበረ የቅርብ ጊዜ ታሪክ ሲሆን
አንጋፋው ሺዋ ሰሜን ፣ ምዕራብና ምሥራቅ በሚል ለመጀመሪያ ጊዜ በሦስት አስተዳደር በመከፋፈል
ናዝሬትን ፣ አምቦንና ደብረ ብርሃን በዋና ዋና ከተማ በመደልደል አዲስ አበባን ራሱን በማስቻል መዋቅሩ
ተዘረጋ ፡፡ ይኸን ተከትሎ ቤተ ክርስቲያንም በመንግሥት የተዘረጋውን መዋቅር በመከተል ሰላሌንና
አሁን ያሉትን ወረዳዎች በማካተት መንበረ ጵጵስናውን በተጉለትና ቡልጋ አውራጃ ዋና ከተማ በደብረ
ብርሃን ቅድስት ሥላሴ ቅጥር ግቢ በመክፈት ሥራውን በይፋ ጀመረ ፡፡
        </p>
        <p class='p'>ስለ ታሪካዊ አመጣጡ ይኸን ያኽል ካቀረብን ሺዋን ሳይነጣጠልና አንድ ሆኖ እያለ እነማን ሊቃነ ጳጳሳት
            ይመሩት ነበረ የሚለውን ስንመለከት ከዳግማዊ ዐፄ ምኒልክ ጀምሮ እስከ ንግሥተ ንግሥታት ዘውዲቱ
            ምኒልክ ዘመን ድረስ በግብፃዊ በኋላም ወደው ፈቅደው ኢትዮጵያዊ በሆኑት በብፁዕ አቡነ ማቴዎስ ይመራ
            ነበር ፡፡ ከዚያም በኢትዮጵያ የመጨረሻው ግብፃዊ ሊቀ ጳጳሳት በብፁዕ አቡነ ቄርሎስ ይመራ ነበር ፤
            በ1921 ዓ.ም 4 ኢትዮጵያውያን እና በኋላም በጥር 1 ቀን 1922 ዓ.ም የግብፁ ፓትርያርክ ለጉብኝት
            በመጡ ጊዜ በሕመም ምክንያት ወደ ግብፅ ያልሔዱት ብፁዕ አቡነ ሳዊሮስ 1 በድምሩ 5 ጳጳሳት በአንድ
            ጊዜ ቢሾሙም ሺዋን የሚመሩት አቡነ ቄርሎስ ነበሩ ፡፡ በኢጣሊያ ወረራ ጊዜ ብፁዕ አቡነ ቄርሎስ
            ኢትዮጵያን ትተው ወደ ሮም በኋላም ወደ ግብፅ ሄደው በመቅረታቸው ምክንያት ከአምስቱ ኢትዮጵያውያን ጳጳሳት መካከል ብፁዕ አቡነ አብርሃም ሊቀ ጳጳሳት ሆነው በመንበሩ የተተኩ ሲሆን ሌሎች
            ሊቃነ ጳጳሳትንም እንዲሾሙ ተወሰነ፡፡ በ1930 ዓ.ም ከተሾሙት አምስት ጳጳሳት መካከል አንዱ ብፁዕ
            አቡነ ዮሐንስ (ሐዲስ ተክሌ) የመላው ሺዋ ጳጳስ ሆነው ተመድበዋል፡፡ ብፁዕ አቡነ አብርሃም ሐምሌ 14
            ቀን 1931 ዓ.ም ስላረፉ ሊቀ ጳጳስ ሆነው ብፁዕ አቡነ ዮሐንስ የመላው ሺዋ ጳጳስ ሆነው በመንበሩ ተተኩ
            እርሳቸውም በ1934 ዓ.ም በመስከረም ወር ከሾሟቸው አምስት ኢትዮጵያውያን ጳጳሳት መካከል አንዱ
            ብፁዕ አቡነ ማትያስ የመላው ሺዋ ጳጳስ ሆነው ተሾሙ፡፡
        </p>
